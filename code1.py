# Declare variables of different data types
my_integer = 42
my_float = 3.14
my_string = "Hello, World!"
my_list = [1, 2, 3, 4]
my_tuple = (10, 20, 30)
my_dict = {"name": "Alice", "age": 30}
my_set = {1, 2, 3, 4}
is_sunny = True
is_raining = False
# Perform basic operations
my_list.append(5) # Add an element to the list
third_element = my_list[2] # Access an element by index (should be 3)
x, y, z = my_tuple # Unpack the tuple
my_dict["city"] = "Wonderland" # Add a key-value pair to the dictionary
my_set.add(5) # Add an element to the set
# Let's print out some results
print(f"Integer: {my_integer}")
print(f"Float: {my_float}")
print(f"String: {my_string}")
print(f"List: {my_list}")
print(f"Third element of the list: {third_element}")
print(f"Unpacked tuple: x={x}, y={y}, z={z}")
print(f"Dictionary: {my_dict}")
print(f"Set: {my_set}")
print(f"Is it sunny? {is_sunny}")
print(f"Is it raining? {is_raining}")